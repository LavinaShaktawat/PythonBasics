# File: divmod.py
'''takes two parameters and returns a tuple containing the quotient  and the remainder when argument1 (divident) is divided by argument2 (divisor).In other words, it returns the floor division and the modulus of the two numbers.
Syntax:- divmod(divident, divisor)
divident	A Number. The number you want to divide
divisor	    A Number. The number you want to divide with'''

print(divmod(3,7))
#Output:- (0, 3)

print(divmod(7,3))
#Output:- (2, 1)

print(divmod(3.6,3))
#Output:- (1.0, 0.6000000000000001)