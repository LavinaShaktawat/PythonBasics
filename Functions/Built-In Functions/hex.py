# File: hex.py 
'''The hex() function converts the specified number into a hexadecimal value.
The returned string always starts with the prefix 0x.
Syntax: hex(number)
number 		an Integer'''

print(hex(1))
#Output: 0x1

print(hex(0))
#Output: 0x0

print(hex(False))
#Output: 0x0

print(hex(-256))
#Output: -0x100


